public class tabellerv3 {
    public static void main(String[] args) {
        for (int tabel =2; tabel<=10; tabel++)
        {
            System.out.println("tabel for "+tabel);
            for (int n =1; n<=10; n++)
                System.out.println(tabel+"*"+n+"="+tabel*n);
            System.out.println();
        }

    }
}
